    public void initializeFields(String mutables){
        mutable_acids = mutables.split(mutantScreeningWizardAction.DELIM);
        mutable_acidsJList.setListData(mutable_acids);
        amino_acidJList.setListData(amino_acids);
        initConstraints();
        mutation_constraintsTxtArea.setText(constraintsToString());
    }
    
    private void initConstraints(){
        constraints = new String[mutable_acids.length];
        for(int index = 0; index < constraints.length; index++){
            constraints[index] = "";
        }
    }
    
    private String constraintsToString(){
        String rval = "";
        for(int index = 0; index < constraints.length; index++){
            rval += "\n" + mutable_acids[index] + " : ";
            for(int j =0 ; j < constraints[index].length(); j++){
                rval += constraints[index].charAt(j) + ",";
            }
        }
        return rval.substring("\n".length());
    }

    public String[] getConstraints(){
        return constraints;
    }
    
    private String getSelectedAcids(int[] indices){
        String rval = "";
        for(int index : indices){
            rval += amino_acids[index];
        }
        return rval;
    }
    
